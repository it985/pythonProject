import re


def process_text(text):
    lines = text.strip().split('\n')
    results = []
    original_count = len(lines)
    processed_count = 0

    for line in lines:
        if not line.strip():  # 跳过空行
            continue

        # 找到第一个字母的位置
        match_letter = re.search(r'[A-Za-z]', line)
        if match_letter:
            # 从第一个字母开始截取
            line = line[match_letter.start():]

            # 找到字母-数字模式，使用更宽松的正则表达式
